package com.mlink.iwm.scheduler;

import java.io.IOException;

public class IWMSchedulingFileInputException extends IOException {
	final static long serialVersionUID = 1L;

	public IWMSchedulingFileInputException() {
		super();
	}

	public IWMSchedulingFileInputException(String arg0) {
		super(arg0);
	}

	public IWMSchedulingFileInputException(Throwable arg0) {
		super(arg0);
	}

	public IWMSchedulingFileInputException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
